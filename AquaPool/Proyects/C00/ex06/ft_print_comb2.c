#include <unistd.h>

void	ft_print_comb2(void)
{
	char	p;
	int		a;
	int		b;

	a = -1;
	while (a++ < 98)
	{
		b = a;
		while (b++ < 99)
		{
			p = (a / 10) + '0';
			write(1, &p, 1);
			p = (a % 10) + '0';
			write(1, &p, 1);
			write(1, " ", 1);
			p = (b / 10) + '0';
			write(1, &p, 1);
			p = (b % 10) + '0';
			write(1, &p, 1);
			if (a < 98)
				write(1, ", ", 2);
		}
	}
}
