#include "libft.h"

char	*ft_strdup(const char *src)
{
	char			*str;
	unsigned int	len;
	unsigned int	i;

	i = 0;
	len = 0;
	while (src[len])
		len++;
	str = (char*)malloc(sizeof(*str) * (len + 1));
	if (str == NULL)
		return (NULL);
	while (i < len)
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
